#include <stdio.h>

int main() {

    // Define a variable
    int x = 10;

    // Declare a variable
    int y;

    // Assign a value to the variable
    y = 20;

    // Define a character
    char a = 'a';
 
    // Print a string
    printf("Hello World\n");

    // Print a number
    printf("%d\n", x);

    // Print another number
    printf("%d\n", y);

    // Print a character
    printf("%c\n", a);
    
    return 0;

}

