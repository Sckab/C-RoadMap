#include <stdio.h>

int main() {

    // Define a variable
    int x = 10;

    // Declare a variable
    int y;

    // Assign a value to the variable
    y = 20;

    // Define a character
    char a = 'a';

    // Define a string
    char name[] = "Giuliano";

    // define a number
    int age = 16;

    // define another char
    char favoriteProgrammingLanguage = 'C';
 
    // Print a string
    printf("Hello World\n");

    // Print a number
    printf("%d\n", x);

    // Print another number
    printf("%d\n", y);

    // Print a character
    printf("%c\n\n", a);

    // Print my informations
    printf("My name is %s\n", name);
    printf("My age is %d\n", age);
    printf("My favorite programming language is %c\n", favoriteProgrammingLanguage);
    
    return 0;

}

